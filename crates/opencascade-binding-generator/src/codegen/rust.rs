/// Rust keywords that need special handling
const RUST_KEYWORDS: &[&str] = &[
    "as", "break", "const", "continue", "crate", "else", "enum", "extern", "false", "fn", "for",
    "if", "impl", "in", "let", "loop", "match", "mod", "move", "mut", "pub", "ref", "return",
    "self", "Self", "static", "struct", "super", "trait", "true", "type", "unsafe", "use",
    "where", "while", "async", "await", "dyn", "abstract", "become", "box", "do", "final",
    "macro", "override", "priv", "typeof", "unsized", "virtual", "yield", "try",
];

/// Convert a doc comment string to /// format instead of #[doc = "..."] format
fn format_doc_comment(comment: &str) -> String {
    comment
        .lines()
        .map(|line| format!("/// {}", line.trim()))
        .collect::<Vec<_>>()
        .join("\n")
}

/// Generate a file header with generation info and processed headers
fn generate_file_header(module_name: &str, headers: &[String]) -> String {
    let header_list = headers
        .iter()
        .map(|h| format!("//! - {}", h))
        .collect::<Vec<_>>()
        .join("\n");
    
    format!(
        "//! Generated CXX bridge for OpenCASCADE {} module\n//!\n//! This file was automatically generated by opencascade-binding-generator\n//! from the following OCCT headers:\n//!\n{}\n//!\n//! Do not edit this file directly.",
        module_name, header_list
    )
}

/// Generate source attribution for a declaration (header and C++ identifier)
fn format_source_attribution(header: &str, cpp_name: &str) -> String {
    format!("/// **Source:** `{}` - `{}`", header, cpp_name)
}

/// Generate doc comments in /// format (raw string approach for better control)
fn generate_doc_comments(source_header: &Option<String>, cpp_identifier: &str, comment: Option<&str>) -> String {
    let mut result = String::new();
    
    // Always add source attribution
    if let Some(header) = source_header {
        result.push_str(&format!("/// **Source:** `{}` - `{}`\n", header, cpp_identifier));
    } else {
        result.push_str(&format!("/// **Source:** `{}`\n", cpp_identifier));
    }
    
    // Add comment if present
    if let Some(comment_text) = comment {
        result.push_str("///\n"); // Empty line separator
        for line in comment_text.lines() {
            result.push_str(&format!("/// {}\n", line.trim()));
        }
    }
    
    result
}

/// Convert TokenStream to formatted string with basic newlines
fn format_tokenstream(tokens: &TokenStream) -> String {
    let code = tokens.to_string();
    
    // Add basic formatting by inserting newlines in key places to make it more readable  
    let formatted = code
        .replace(" # ! [", "\n#![")
        .replace(" # [cxx :: bridge]", "\n#[cxx::bridge]")
        .replace(" pub (crate) mod ffi {", "\npub(crate) mod ffi {")
        .replace(" unsafe extern \"C++\" {", "\n    unsafe extern \"C++\" {")
        .replace(" } }", "\n    }\n}")
        .replace(" pub use ffi ::", "\npub use ffi::")
        .replace(" impl ", "\nimpl ");
    
    formatted
}

/// Post-process generated code after rustfmt formatting:
/// 1. Convert #[doc = "..."] attributes to /// ... comments for better readability
/// 2. Convert SECTION: markers to regular // comments
/// 3. Convert BLANK_LINE markers to actual blank lines
pub fn postprocess_generated_code(code: &str) -> String {
    use regex::Regex;
    
    // First, convert all #[doc = "..."] and #[doc = r"..."] to /// ... format
    // This regex captures leading whitespace and the content
    // We use (?s) to make . match newlines
    // The r? makes the raw string prefix optional
    // The content pattern matches: non-quote chars OR escaped quotes (backslash followed by quote)
    let doc_attr_re = Regex::new(r#"(?s)([ \t]*)#\[doc = r?"((?:[^"\\]|\\.)*)"\]"#).unwrap();
    let result = doc_attr_re.replace_all(code, |caps: &regex::Captures| {
        let indent = &caps[1];
        let content = &caps[2];
        // Handle empty doc comments
        if content.is_empty() {
            format!("{}///", indent)
        } else {
            // Handle escape sequences from quote! macro:
            // - \n becomes actual newline
            // - \" becomes "
            // - \\ becomes \
            let unescaped = content
                .replace("\\n", "\n")
                .replace("\\\"", "\"")
                .replace("\\\\", "\\");
            unescaped
                .lines()
                .map(|line| {
                    let trimmed = line.trim();
                    if trimmed.is_empty() {
                        format!("{}///", indent)
                    } else {
                        format!("{}/// {}", indent, trimmed)
                    }
                })
                .collect::<Vec<_>>()
                .join("\n")
        }
    });
    
    // Convert BLANK_LINE markers to actual blank lines
    // Pattern: /// BLANK_LINE (possibly with trailing whitespace) -> empty line
    let blank_re = Regex::new(r#"[ \t]*/// BLANK_LINE[ \t]*\n"#).unwrap();
    let result = blank_re.replace_all(&result, "\n");
    
    // Now convert SECTION: markers to regular comments (not doc comments)
    // Pattern: /// SECTION: ... -> // ...
    let section_re = Regex::new(r#"/// SECTION: ([^\n]*)"#).unwrap();
    let result = section_re.replace_all(&result, "// $1");
    
    result.into_owned()
}

/// Convert a method name to a safe Rust identifier, adding underscore suffix for keywords
/// Note: CXX doesn't support raw identifiers (r#keyword), so we use a suffix instead
fn safe_method_name(name: &str) -> String {
    let snake_name = name.to_snake_case();
    if RUST_KEYWORDS.contains(&snake_name.as_str()) {
        format!("{}_", snake_name)  // Add trailing underscore for keywords
    } else {
        snake_name
    }
}

/// Convert a parameter name to a safe Rust identifier
/// Rust keywords are renamed with trailing underscore (e.g., "where" -> "where_")
/// because CXX doesn't support r#keyword syntax in FFI declarations
fn safe_param_ident(name: &str) -> proc_macro2::Ident {
    if RUST_KEYWORDS.contains(&name) {
        format_ident!("{}_", name)
    } else {
        format_ident!("{}", name)
    }
}

/// Generate Rust CXX bridge code for a module
///
/// Returns a String with properly formatted documentation.
pub fn generate_module(
    module: &Module,
    classes: &[&ParsedClass],
    enums: &[&ParsedEnum],
    functions: &[&ParsedFunction],
    headers: &[String],
    collections: &[super::collections::CollectionInfo],
    symbol_table: &crate::resolver::SymbolTable,
) -> String {
    let _module_name = format_ident!("{}", module.rust_name);
    // Use wrapper_ prefix to avoid collision with OCCT headers (e.g., gp.hxx)
    let include_file = format!("wrapper_{}.hxx", module.rust_name);

    // Use data from symbol table
    let all_enum_names = &symbol_table.all_enum_names;
    let all_class_names = &symbol_table.all_class_names;
    let cross_module_types = symbol_table.cross_module_types_for_module(&module.rust_name);

    // Build the set of classes defined in this module
    let module_classes: HashSet<String> = classes.iter().map(|c| c.name.clone()).collect();

    // Create type context for this module
    let type_ctx = TypeContext {
        current_module: &module.rust_name,
        module_classes: &module_classes,
        all_enums: all_enum_names,
        all_classes: all_class_names,
    };

    // Get ALL classes with protected destructors (from symbol table)
    let protected_destructor_class_names = symbol_table.protected_destructor_class_names();

    // Generate cross-module type aliases
    // Skip enums (enum generation is disabled) and classes with protected destructors (not declared)
    let non_enum_cross_module_types: Vec<_> = cross_module_types
        .iter()
        .filter(|cmt| !cmt.is_enum)
        .filter(|cmt| !protected_destructor_class_names.contains(&cmt.cpp_name))
        .cloned()
        .collect();
    let type_aliases = generate_type_aliases(&non_enum_cross_module_types);

    // Collect all referenced types that need opaque declarations
    let collected_types = collect_referenced_types(classes);
    
    let opaque_type_decls =
        generate_opaque_type_declarations(&collected_types, classes, cross_module_types, all_enum_names);

    // NOTE: Enum generation is disabled because OCCT uses unscoped enums (plain `enum`)
    // while CXX only supports `enum class`. We skip enums for now.
    // TODO: Implement enum support by treating them as integer types with const values
    let enum_items: Vec<TokenStream> = Vec::new();
    let _ = enums; // suppress unused warning

    // Generate type declarations and methods for each class
    // Skip classes with protected destructors - CXX can't handle them
    let class_items = classes
        .iter()
        .filter(|class| !class.has_protected_destructor)
        .map(|class| generate_class(class, &type_ctx, symbol_table))
        .collect::<Vec<_>>();

    // Generate namespace-level free functions
    let function_items = generate_functions(functions, &type_ctx);

    // Filter classes for re-exports (skip protected destructor classes)
    let bindable_classes: Vec<&ParsedClass> = classes
        .iter()
        .filter(|class| !class.has_protected_destructor)
        .cloned()
        .collect();

    // Generate impl UniquePtr blocks for cross-module types
    let unique_ptr_impls = generate_unique_ptr_impls(&bindable_classes);

    // Generate re-exports for classes, enums, and functions
    // Use all-modules protected destructor class names for filtering upcast targets
    let re_exports = generate_re_exports(&module.name, &bindable_classes, enums, functions, all_class_names, &protected_destructor_class_names, all_enum_names, symbol_table);

    // Generate Handle impl blocks with upcast methods
    let handle_impls = generate_handle_impls(&bindable_classes, all_class_names, &protected_destructor_class_names, symbol_table);

    // Generate to_handle free function exports
    let to_handle_exports = generate_to_handle_exports(&bindable_classes);

    // Generate the file header
    let file_header = generate_file_header(&module.name, headers);

    // Build section headers conditionally - only when there's content
    // Doc attributes need an item to attach to, so we prepend them to the first item in the section
    let enum_section = if !enum_items.is_empty() {
        quote! {
            #[doc = " BLANK_LINE"]
            #[doc = " SECTION: ========================"]
            #[doc = " SECTION: Shared enums"]
            #[doc = " SECTION: ========================"]
            #[doc = " BLANK_LINE"]
            #(#enum_items)*
        }
    } else {
        quote! {}
    };

    let class_section = if !class_items.is_empty() {
        quote! {
            #[doc = " BLANK_LINE"]
            #[doc = " SECTION: ========================"]
            #[doc = " SECTION: Module types and methods"]
            #[doc = " SECTION: ========================"]
            #[doc = " BLANK_LINE"]
            #(#class_items)*
        }
    } else {
        quote! {}
    };

    let function_section = if !function_items.is_empty() {
        quote! {
            #[doc = " BLANK_LINE"]
            #[doc = " SECTION: ========================"]
            #[doc = " SECTION: Free functions"]
            #[doc = " SECTION: ========================"]
            #[doc = " BLANK_LINE"]
            #(#function_items)*
        }
    } else {
        quote! {}
    };

    let alias_section = if !type_aliases.is_empty() {
        quote! {
            #[doc = " BLANK_LINE"]
            #[doc = " SECTION: ========================"]
            #[doc = " SECTION: Cross-module type aliases"]
            #[doc = " SECTION: ========================"]
            #[doc = " BLANK_LINE"]
            #(#type_aliases)*
        }
    } else {
        quote! {}
    };

    let opaque_section = if !opaque_type_decls.is_empty() {
        quote! {
            #[doc = " BLANK_LINE"]
            #[doc = " SECTION: ========================"]
            #[doc = " SECTION: Referenced types (opaque)"]
            #[doc = " SECTION: ========================"]
            #[doc = " BLANK_LINE"]
            #(#opaque_type_decls)*
        }
    } else {
        quote! {}
    };

    // Assemble the module as TokenStream first
    let tokens = quote! {
        #![allow(dead_code)]
        #![allow(non_snake_case)]
        #![allow(clippy::missing_safety_doc)]

        // Re-exports
        #(#re_exports)*

        // to_handle free function exports
        #(#to_handle_exports)*

        // Handle type impl blocks
        #(#handle_impls)*

        #[cxx::bridge]
        pub(crate) mod ffi {
            #enum_section

            unsafe extern "C++" {
                include!(#include_file);

                #class_section
                #function_section
                #alias_section
                #opaque_section
            }

            #(#unique_ptr_impls)*
        }
    };

    // Convert to string and apply doc comment conversion
    let mut tokens_string = format_tokenstream(&tokens);
    
    // Insert collection FFI code if this module has collections
    if !collections.is_empty() {
        let (mut coll_type_aliases, coll_ffi_decls) = 
            super::collections::generate_module_rust_ffi_collections(collections, &module.rust_name);
        
        // Filter out type aliases that already exist in the generated code
        // (e.g., if this module already has `type TopoDS_Shape = ...` we don't need to add it again)
        let filtered_aliases: Vec<&str> = coll_type_aliases
            .lines()
            .filter(|line| {
                // Check if this is a type alias line
                if line.contains("type ") && line.contains(" = ") {
                    // Extract the type name (e.g., "TopoDS_Shape" from "type TopoDS_Shape = ...")
                    if let Some(type_start) = line.find("type ") {
                        let after_type = &line[type_start + 5..];
                        if let Some(eq_pos) = after_type.find(" = ") {
                            let type_name = after_type[..eq_pos].trim();
                            // Check if this type already exists in the tokens
                            let pattern = format!("type {} = ", type_name);
                            return !tokens_string.contains(&pattern);
                        }
                    }
                }
                true // Keep non-type-alias lines (comments etc)
            })
            .collect();
        coll_type_aliases = filtered_aliases.join("\n");
        if !coll_type_aliases.is_empty() {
            coll_type_aliases.push('\n');
        }
        
        // Insert collection code into the extern C++ block
        // The quote! macro generates unformatted code like: "}\nimpl UniquePtr < ... >"
        // We want to insert just before the "}" that closes the unsafe extern "C++" block
        // This "}" is followed immediately by "impl UniquePtr"
        if let Some(impl_unique_pos) = tokens_string.find("impl UniquePtr") {
            // Search backwards from impl UniquePtr for the closing "}" of the extern block
            let search_region = &tokens_string[..impl_unique_pos];
            if let Some(closing_brace) = search_region.rfind('}') {
                // Insert collection code just before the closing brace
                // Need to add proper formatting
                let collection_code = format!(
                    "{}{}\n",
                    coll_type_aliases,
                    coll_ffi_decls
                );
                tokens_string.insert_str(closing_brace, &collection_code);
            }
        }
        
        // Append collection impl blocks after the ffi module
        let coll_impls = super::collections::generate_module_rust_impl_collections(collections, &module.rust_name);
        tokens_string.push_str("\n");
        tokens_string.push_str(&coll_impls);
    }
    
    format!("{}\n\n{}", file_header, tokens_string)
}

/// Types collected from class interfaces
struct CollectedTypes {
    /// Class types (e.g., "gp_Pnt", "Geom_TrimmedCurve") - sorted for deterministic output
    classes: BTreeSet<String>,
    /// Handle types with their inner class (e.g., "Geom_TrimmedCurve" for Handle<Geom_TrimmedCurve>) - sorted for deterministic output
    handles: BTreeSet<String>,
}

/// Collect all referenced OCCT types from class methods and constructors
