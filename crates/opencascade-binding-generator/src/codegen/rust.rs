/// Generate type alias declarations for cross-module types
fn generate_type_aliases(cross_module_types: &[CrossModuleType]) -> Vec<TokenStream> {
    use crate::type_mapping::safe_short_name;

    cross_module_types
        .iter()
        .map(|cmt| {
            let cpp_name = &cmt.cpp_name;
            let source_module = format_ident!("{}", cmt.source_module);
            let doc_comment = format!("{} from {} module", cmt.rust_name, cmt.source_module);

            // For enums, use the short name (enums are now declared with short Rust names)
            // For classes, use the short name (possibly with trailing underscore for reserved names)
            let rust_name_in_source = if cmt.is_enum {
                // Extract short name from full C++ name (e.g., "TopAbs_ShapeEnum" -> "ShapeEnum")
                let short_name = cpp_name
                    .find('_')
                    .map(|pos| &cpp_name[pos + 1..])
                    .unwrap_or(cpp_name);
                format_ident!("{}", short_name)
            } else {
                let internal_name = safe_short_name(&cmt.rust_name);
                format_ident!("{}", internal_name)
            };

            // Generate: type gp_Vec = crate::gp::ffi::Vec_;
            let alias_path = quote! { crate::#source_module::ffi::#rust_name_in_source };
            let cpp_name_ident = format_ident!("{}", cpp_name);

            quote! {
                #[doc = #doc_comment]
                type #cpp_name_ident = #alias_path;
            }
        })
        .collect()
}

/// Generate CXX shared enum declaration
fn generate_enum(enum_decl: &ParsedEnum) -> TokenStream {
    let cpp_enum_name = &enum_decl.name; // e.g., "TopAbs_ShapeEnum"
    
    // Extract short name by removing module prefix (e.g., "ShapeEnum" from "TopAbs_ShapeEnum")
    let short_name = cpp_enum_name
        .strip_prefix(&format!("{}_", enum_decl.module))
        .unwrap_or(cpp_enum_name);
    let enum_name_ident = format_ident!("{}", short_name);

    // Doc comment
    let doc = enum_decl
        .comment
        .as_ref()
        .map(|c| quote! { #[doc = #c] })
        .unwrap_or_default();

    // Generate variants with short Rust names and #[cxx_name] attributes
    let variants = enum_decl.variants.iter().map(|v| {
        let cpp_variant_name = &v.name; // e.g., "TopAbs_COMPSOLID"
        
        // Convert to Rust-style PascalCase by:
        // 1. Remove enum prefix (e.g., "TopAbs_COMPSOLID" -> "COMPSOLID")
        // 2. Convert SCREAMING_SNAKE_CASE to PascalCase
        let rust_variant_name = cpp_variant_name
            .strip_prefix(&format!("{}_", enum_decl.module))
            .unwrap_or(cpp_variant_name);
        let rust_variant_name = screaming_snake_to_pascal_case(rust_variant_name);
        let variant_ident = format_ident!("{}", rust_variant_name);
        
        let variant_doc = v
            .comment
            .as_ref()
            .map(|c| quote! { #[doc = #c] })
            .unwrap_or_default();

        quote! {
            #variant_doc
            #[cxx_name = #cpp_variant_name]
            #variant_ident
        }
    });

    quote! {
        #doc
        #[derive(Debug, Clone, Copy, PartialEq, Eq)]
        #[cxx_name = #cpp_enum_name]
        enum #enum_name_ident {
            #(#variants),*
        }
    }
}

/// Convert SCREAMING_SNAKE_CASE to PascalCase
/// e.g., "COMPSOLID" -> "CompSolid", "SHAPE" -> "Shape"
fn screaming_snake_to_pascal_case(s: &str) -> String {
    s.split('_')
        .filter(|part| !part.is_empty())
        .map(|part| {
            let mut chars = part.chars();
            match chars.next() {
                None => String::new(),
                Some(first) => {
                    first.to_uppercase().collect::<String>() + &chars.as_str().to_lowercase()
                }
            }
        })
        .collect()
}

/// Generate CXX declarations for a single class
fn generate_class(
    class: &ParsedClass,
    ctx: &TypeContext,
    symbol_table: &crate::resolver::SymbolTable,
) -> TokenStream {
    let cpp_name = &class.name;
    // Use safe short name (e.g., "Pnt" instead of "gp_Pnt", or "GpVec" for "Vec")
    let short_name = class.safe_short_name();
    let rust_name = format_ident!("{}", short_name);

    // Doc comment with source attribution
    let source_attr = format_source_attribution(&class.source_header, &class.name);
    let doc = if let Some(ref comment) = class.comment {
        quote! { 
            #[doc = #source_attr]
            #[doc = ""]  
            #[doc = #comment] 
        }
    } else {
        quote! { #[doc = #source_attr] }
    };

    // Type declaration - use short Rust name, map to full C++ name
    let type_decl = quote! {
        #doc
        #[cxx_name = #cpp_name]
        type #rust_name;
    };

    // Constructor functions
    let ctors = generate_constructors(class, ctx);

    // Instance methods (that can be called directly)
    let methods = generate_methods(class, ctx);

    // Wrapper methods (free functions for methods returning by value)
    let wrapper_methods = generate_wrapper_methods(class, ctx);

    // Static methods
    let static_methods = generate_static_methods(class, ctx);

    // Upcast functions (for inheritance)
    let upcast_methods = generate_upcast_methods(class, ctx, symbol_table);

    // Inherited methods from base classes
    // NOTE: Disabled due to CXX method pointer verification issue.
    // CXX generates: `bool (DerivedClass::*)(args) = &DerivedClass::Method`
    // but inherited methods have method pointers like `bool (BaseClass::*)(args)`.
    // This causes C++ compilation errors for any inherited method.
    // Users can upcast to base type to access these methods.
    let inherited_methods: Vec<TokenStream> = Vec::new();
    // let inherited_methods = generate_inherited_methods(class, all_classes_map, ctx);

    // to_owned function (copy constructor via CXX's construct_unique)
    let to_owned = generate_to_owned(class);

    // to_handle function (for transient classes - wraps object in Handle)
    let to_handle = generate_to_handle_ffi(class);

    // Handle upcast functions (for Handle types to upcast to base Handle types)
    let handle_upcasts = generate_handle_upcast_ffi(class, ctx, symbol_table);

    // Section header as doc comment (will render as /// in output)
    let section_line = format!(" ======================== {} ========================", cpp_name);

    quote! {
        #[doc = #section_line]
        #type_decl

        #(#ctors)*
        #(#methods)*
        #(#wrapper_methods)*
        #(#static_methods)*
        #(#upcast_methods)*
        #(#inherited_methods)*
        #to_owned
        #to_handle
        #(#handle_upcasts)*
    }
}

/// Generate constructor function declarations
fn generate_constructors(class: &ParsedClass, ctx: &TypeContext) -> Vec<TokenStream> {
    // Skip constructors for classes with protected destructors (non-instantiable abstract bases)
    // CXX UniquePtr requires a public destructor
    if class.has_protected_destructor {
        return Vec::new();
    }
    
    // Skip constructors for abstract classes (has pure virtual methods)
    if class.is_abstract {
        return Vec::new();
    }
    
    // Group constructors by overload suffix to handle naming conflicts
    let mut ctor_names: HashMap<String, usize> = HashMap::new();

    class
        .constructors
        .iter()
        .filter(|ctor| {
            // Skip constructors with class/handle parameters passed by value (not supported by CXX)
            if ctor.params.iter().any(|p| matches!(&p.ty, Type::Class(_) | Type::Handle(_))) {
                return false;
            }
            // Skip constructors with unbindable types (C strings, streams, void pointers, etc.)
            if ctor.has_unbindable_types() {
                return false;
            }
            // Skip constructors with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
            if constructor_uses_enum(ctor, ctx.all_enums) {
                return false;
            }
            true
        })
        .map(|ctor| {
            let base_suffix = ctor.overload_suffix();
            let suffix = if base_suffix.is_empty() {
                "ctor".to_string()
            } else {
                format!("ctor{}", base_suffix)
            };

            // Handle duplicate suffixes by adding a number
            let count = ctor_names.entry(suffix.clone()).or_insert(0);
            *count += 1;
            let final_suffix = if *count > 1 {
                format!("{}_{}", suffix, count)
            } else {
                suffix
            };

            generate_constructor(class, ctor, &final_suffix, ctx)
        })
        .collect()
}

/// Generate a single constructor function
fn generate_constructor(class: &ParsedClass, ctor: &Constructor, suffix: &str, ctx: &TypeContext) -> TokenStream {
    let cpp_name = &class.name;
    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);
    let cpp_wrapper_name = format!("{}_{}", cpp_name, suffix);
    let rust_fn_name = format_ident!(
        "{}_{}",
        short_name,
        suffix.to_snake_case()
    );

    // Parameters - use original C++ names, escaping keywords
    let params = ctor.params.iter().map(|p| {
        let name = safe_param_ident(&p.name);
        let ty = map_type_in_context(&p.ty, ctx);
        let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { #name: #ty_tokens }
    });

    // Doc comment with source attribution
    let source_attr = format_source_attribution(&class.source_header, &format!("{}::{}()", &class.name, &class.name));
    let doc = if let Some(ref comment) = ctor.comment {
        quote! { 
            #[doc = #source_attr]
            #[doc = ""]  
            #[doc = #comment] 
        }
    } else {
        quote! { #[doc = #source_attr] }
    };

    quote! {
        #doc
        #[cxx_name = #cpp_wrapper_name]
        fn #rust_fn_name(#(#params),*) -> UniquePtr<#rust_type>;
    }
}

/// Check if a method returns a class type by value (needs wrapper function)
/// Enum returns don't need wrappers since CXX handles shared enums directly.
fn needs_wrapper_function(method: &Method, all_enums: &std::collections::HashSet<String>) -> bool {
    // Methods with const char* parameters need wrappers to convert from rust::Str
    if method.params.iter().any(|p| p.ty.is_c_string()) {
        return true;
    }
    
    // Methods returning const char* need wrappers to convert to rust::String
    if method.return_type.as_ref().map(|t| t.is_c_string()).unwrap_or(false) {
        return true;
    }
    
    method
        .return_type
        .as_ref()
        .map(|ty| {
            // Check if it's a class or handle
            let is_class_or_handle = ty.is_class() || ty.is_handle();
            // But exclude enums - they don't need wrappers
            let is_enum = match ty {
                Type::Class(name) => all_enums.contains(name),
                _ => false,
            };
            is_class_or_handle && !is_enum
        })
        .unwrap_or(false)
}

// Additional filter functions - delegate to centralized implementations in resolver module

/// Check if a method has parameters that would be passed by value (not supported by CXX for opaque types)
fn has_unsupported_by_value_params(method: &Method) -> bool {
    resolver::method_has_unsupported_by_value_params(method).is_some()
}

/// Check if a const method returns a mutable reference (not allowed by CXX)
/// CXX requires &mut self when returning &mut, but C++ allows const methods to return non-const refs
fn has_const_mut_return_mismatch(method: &Method) -> bool {
    resolver::has_const_mut_return_mismatch(method)
}

/// Check if a method needs explicit lifetimes (CXX limitation)
/// Returns true if the method returns a mutable reference and has other reference parameters.
/// Rust can't infer lifetimes when there are multiple potential sources.
fn needs_explicit_lifetimes(method: &Method) -> bool {
    resolver::method_needs_explicit_lifetimes(method)
}

/// Generate instance method declarations
fn generate_methods(class: &ParsedClass, ctx: &TypeContext) -> Vec<TokenStream> {
    // Group methods by name to handle overloads
    let mut method_counts: HashMap<String, usize> = HashMap::new();

    class
        .methods
        .iter()
        .filter_map(|method| {
            // Skip methods that need wrapper functions - they're generated separately
            if needs_wrapper_function(method, ctx.all_enums) {
                return None;
            }
            
            // Skip methods with class/handle parameters passed by value (not supported by CXX)
            if has_unsupported_by_value_params(method) {
                return None;
            }

            // Skip methods with unbindable types (streams, void pointers, etc.)
            if method.has_unbindable_types() {
                return None;
            }
            
            // Skip methods with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
            if method_uses_enum(method, ctx.all_enums) {
                return None;
            }
            
            // Skip const methods that return mutable references (CXX doesn't allow this)
            if has_const_mut_return_mismatch(method) {
                return None;
            }

            let count = method_counts.entry(method.name.clone()).or_insert(0);
            *count += 1;

            // Generate a suffix based on parameters for overloaded methods
            let overload_suffix = generate_overload_suffix(method, *count, class);

            generate_method_with_suffix(class, method, &overload_suffix, ctx)
        })
        .collect()
}

/// Generate free function declarations for methods returning class types by value
fn generate_wrapper_methods(class: &ParsedClass, ctx: &TypeContext) -> Vec<TokenStream> {
    // Group methods by name to handle overloads
    let mut method_counts: HashMap<String, usize> = HashMap::new();

    class
        .methods
        .iter()
        .filter_map(|method| {
            // Only process methods that need wrapper functions
            if !needs_wrapper_function(method, ctx.all_enums) {
                return None;
            }
            
            // Skip methods with class/handle parameters passed by value (not supported by CXX)
            if has_unsupported_by_value_params(method) {
                return None;
            }

            // Skip methods with unbindable types (streams, void pointers, etc.)
            if method.has_unbindable_types() {
                return None;
            }
            
            // Skip methods with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
            if method_uses_enum(method, ctx.all_enums) {
                return None;
            }
            
            // Skip const methods that return mutable references (CXX doesn't allow this)
            if has_const_mut_return_mismatch(method) {
                return None;
            }

            // Skip methods that need explicit lifetimes (return mut ref + have ref params)
            if needs_explicit_lifetimes(method) {
                return None;
            }

            let count = method_counts.entry(method.name.clone()).or_insert(0);
            *count += 1;

            // Generate a suffix based on parameters for overloaded methods
            let overload_suffix = generate_overload_suffix_for_wrappers(method, *count, class, ctx);

            generate_wrapper_method(class, method, &overload_suffix, ctx)
        })
        .collect()
}

/// Generate a suffix for overloaded wrapper methods
fn generate_overload_suffix_for_wrappers(method: &Method, _count: usize, class: &ParsedClass, ctx: &TypeContext) -> String {
    // Count how many wrapper methods have this name
    let same_name_count = class
        .methods
        .iter()
        .filter(|m| m.name == method.name && needs_wrapper_function(m, ctx.all_enums))
        .count();

    // Check if there are any non-wrapper methods with the same name
    // (These would conflict with the wrapper impl method name)
    let has_non_wrapper_with_same_name = class
        .methods
        .iter()
        .any(|m| m.name == method.name && !needs_wrapper_function(m, ctx.all_enums)
            && !has_unsupported_by_value_params(m)
            && !m.has_unbindable_types()
            && !method_uses_enum(m, ctx.all_enums)
            && !has_const_mut_return_mismatch(m));

    if same_name_count <= 1 && !has_non_wrapper_with_same_name {
        return String::new();
    }

    // Use the method's overload_suffix which is based on all parameter types
    let base_suffix = method.overload_suffix();
    
    // Check if there's another method with the same name and parameter types but different const-ness
    // This handles the case where we have both const and non-const versions of the same method
    let has_const_mut_pair = class
        .methods
        .iter()
        .filter(|m| m.name == method.name && needs_wrapper_function(m, ctx.all_enums))
        .any(|m| m.overload_suffix() == base_suffix && m.is_const != method.is_const);
    
    if has_const_mut_pair && !method.is_const {
        // Non-const (mutable) version gets _mut suffix
        format!("{}_mut", base_suffix)
    } else {
        base_suffix
    }
}

/// Generate a wrapper method (free function that calls C++ wrapper)
fn generate_wrapper_method(
    class: &ParsedClass,
    method: &Method,
    suffix: &str,
    ctx: &TypeContext,
) -> Option<TokenStream> {
    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);

    // C++ wrapper function name: ClassName_MethodName
    let cpp_wrapper_name = format!("{}_{}", class.name, method.name);

    // Rust function name: ClassName_method_name with optional suffix
    // suffix is already in snake_case with leading underscore (e.g., "_pnt", "_box_trsf")
    let rust_name = if suffix.is_empty() {
        format_ident!("{}_{}", short_name, method.name.to_snake_case())
    } else {
        format_ident!(
            "{}_{}{}" ,
            short_name,
            method.name.to_snake_case(),
            suffix
        )
    };

    // Self parameter (passed by reference as first param)
    // Use "self_" not "this" because "this" is a C++ keyword
    let self_param = if method.is_const {
        quote! { self_: &#rust_type }
    } else {
        quote! { self_: Pin<&mut #rust_type> }
    };

    // Other parameters - use original C++ names, escaping keywords
    let params = method.params.iter().map(|p| {
        let name = safe_param_ident(&p.name);
        let ty = map_type_in_context(&p.ty, ctx);
        let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { #name: #ty_tokens }
    });

    // Return type
    let return_type = method.return_type.as_ref().map(|ty| {
        let mapping = map_return_type_in_context(ty, ctx);
        let ty_tokens: TokenStream = mapping.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { -> #ty_tokens }
    });

    // Doc comment
    let doc = method
        .comment
        .as_ref()
        .map(|c| quote! { #[doc = #c] })
        .unwrap_or_default();

    Some(quote! {
        #doc
        #[cxx_name = #cpp_wrapper_name]
        fn #rust_name(#self_param, #(#params),*) #return_type;
    })
}

/// Generate a suffix for overloaded methods based on their parameter types
fn generate_overload_suffix(method: &Method, _count: usize, class: &ParsedClass) -> String {
    // Count how many methods have this name
    let same_name_count = class.methods.iter().filter(|m| m.name == method.name).count();

    if same_name_count <= 1 {
        // No overloads, no suffix needed
        return String::new();
    }

    // Use the method's overload_suffix which is based on all parameter types
    let base_suffix = method.overload_suffix();
    
    // Check if there's another method with the same name and parameter types but different const-ness
    // This handles the case where we have both const and non-const versions of the same method
    let has_const_mut_pair = class
        .methods
        .iter()
        .any(|m| m.name == method.name && m.overload_suffix() == base_suffix && m.is_const != method.is_const);
    
    if has_const_mut_pair && !method.is_const {
        // Non-const (mutable) version gets _mut suffix
        format!("{}_mut", base_suffix)
    } else {
        base_suffix
    }
}

/// Generate a single instance method with optional suffix for overloads
fn generate_method_with_suffix(
    class: &ParsedClass,
    method: &Method,
    suffix: &str,
    ctx: &TypeContext,
) -> Option<TokenStream> {
    let cpp_name = &method.name;
    let base_name = safe_method_name(&method.name);
    // suffix is already in snake_case format with leading underscore (e.g., "_pnt", "_box_trsf")
    // Don't call to_snake_case() which would strip the leading underscore
    let rust_name_str = if suffix.is_empty() {
        base_name
    } else {
        format!("{}{}", base_name, suffix)
    };
    let rust_name = format_ident!("{}", rust_name_str);
    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);

    // Self parameter
    let self_param = if method.is_const {
        quote! { self: &#rust_type }
    } else {
        quote! { self: Pin<&mut #rust_type> }
    };

    // Other parameters - use original C++ names, escaping keywords
    let params = method.params.iter().map(|p| {
        let name = safe_param_ident(&p.name);
        let ty = map_type_in_context(&p.ty, ctx);
        let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { #name: #ty_tokens }
    });

    // Return type
    let return_type = method.return_type.as_ref().map(|ty| {
        let mapping = map_return_type_in_context(ty, ctx);
        let ty_tokens: TokenStream = mapping.rust_type.parse().unwrap_or_else(|_| quote! { () });

        // Methods that return by value need wrappers
        if ty.is_class() || ty.is_handle() {
            // This will require a C++ wrapper - mark it
            quote! { -> #ty_tokens }
        } else {
            quote! { -> #ty_tokens }
        }
    });

    // Doc comment
    let doc = method
        .comment
        .as_ref()
        .map(|c| quote! { #[doc = #c] })
        .unwrap_or_default();

    Some(quote! {
        #doc
        #[cxx_name = #cpp_name]
        fn #rust_name(#self_param, #(#params),*) #return_type;
    })
}

/// Generate a single instance method
#[allow(dead_code)]
fn generate_method(class: &ParsedClass, method: &Method, ctx: &TypeContext) -> Option<TokenStream> {
    generate_method_with_suffix(class, method, "", ctx)
}

/// Generate static method declarations
fn generate_static_methods(class: &ParsedClass, ctx: &TypeContext) -> Vec<TokenStream> {
    // Track method counts for overload disambiguation
    let mut method_counts: HashMap<String, usize> = HashMap::new();
    
    class
        .static_methods
        .iter()
        .filter_map(|method| {
            let count = method_counts.entry(method.name.clone()).or_insert(0);
            *count += 1;
            generate_static_method(class, method, *count, ctx)
        })
        .collect()
}

/// Generate a single static method
fn generate_static_method(
    class: &ParsedClass,
    method: &StaticMethod,
    _occurrence: usize,
    ctx: &TypeContext,
) -> Option<TokenStream> {
    // Skip methods with stream parameters - CXX can't bind C++ streams
    if method.has_unbindable_types() {
        return None;
    }
    
    // Skip methods with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
    if static_method_uses_enum(method, ctx.all_enums) {
        return None;
    }

    // Skip static methods that return bare references - these need lifetimes
    // (e.g., get_type_descriptor() -> &Handle<Standard_Type>)
    if method.return_type.as_ref().map(|t| t.is_reference()).unwrap_or(false) {
        return None;
    }
    
    // Skip static methods with class/handle parameters passed by value (not supported by CXX)
    // CXX doesn't allow passing opaque types by value
    if method.params.iter().any(|p| matches!(&p.ty, Type::Class(_) | Type::Handle(_))) {
        return None;
    }
    
    // Note: Static methods returning const char* are handled via wrappers that return rust::String
    // The type mapping in map_return_type_in_context handles mapping const char* -> String

    let _cpp_name = &method.name;
    let short_name = class.safe_short_name();
    
    // Count how many static methods have this name to determine if suffix is needed
    let static_same_name_count = class
        .static_methods
        .iter()
        .filter(|m| m.name == method.name)
        .count();
    
    // Also check for instance methods with the same name (they generate wrapper functions
    // with the same naming pattern: ClassName_method_name)
    let instance_same_name_exists = class
        .methods
        .iter()
        .any(|m| m.name == method.name);
    
    // Generate overload suffix based on parameter types
    // We need a suffix if:
    // 1. There are multiple static methods with this name, OR
    // 2. There's an instance method with the same name
    let overload_suffix = if static_same_name_count > 1 || instance_same_name_exists {
        method.overload_suffix()
    } else {
        String::new()
    };
    
    let cpp_wrapper_name = format!("{}_{}{}", class.name, method.name, overload_suffix);
    let rust_name = format_ident!(
        "{}_{}{}", 
        short_name,
        method.name.to_snake_case(),
        overload_suffix
    );

    // Parameters - use original C++ names, escaping keywords
    let params = method.params.iter().map(|p| {
        let name = safe_param_ident(&p.name);
        let ty = map_type_in_context(&p.ty, ctx);
        let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { #name: #ty_tokens }
    });

    // Return type
    let return_type = method.return_type.as_ref().map(|ty| {
        let mapping = map_return_type_in_context(ty, ctx);
        let ty_tokens: TokenStream = mapping.rust_type.parse().unwrap_or_else(|_| quote! { () });
        quote! { -> #ty_tokens }
    });

    // Doc comment
    let doc = method
        .comment
        .as_ref()
        .map(|c| quote! { #[doc = #c] })
        .unwrap_or_default();

    Some(quote! {
        #doc
        #[cxx_name = #cpp_wrapper_name]
        fn #rust_name(#(#params),*) #return_type;
    })
}

/// Generate upcast function declarations for inheritance (const and mutable)
/// For each ancestor class, generates functions that cast from derived to ancestor
fn generate_upcast_methods(
    class: &ParsedClass,
    ctx: &TypeContext,
    symbol_table: &crate::resolver::SymbolTable,
) -> Vec<TokenStream> {
    let ancestors = symbol_table.get_all_ancestors_by_name(&class.name);
    let protected_destructor_classes = symbol_table.protected_destructor_class_names();
    
    ancestors
        .iter()
        .flat_map(|base_class| {
            // Skip base classes that aren't in our known set of classes
            // This filters out types from modules we haven't parsed (e.g., XSControl)
            if !ctx.all_classes.contains(base_class) {
                return vec![];
            }
            
            // Skip base classes with protected destructors (they're excluded from bindings)
            if protected_destructor_classes.contains(base_class) {
                return vec![];
            }
            
            // Get the short names for both derived and base class
            let derived_short = class.safe_short_name();
            let derived_ident = format_ident!("{}", derived_short);

            // Extract short name from base class (e.g., "TopoDS_Shape" -> "Shape")
            let base_short = if let Some(underscore_pos) = base_class.find('_') {
                crate::type_mapping::safe_short_name(&base_class[underscore_pos + 1..])
            } else {
                crate::type_mapping::safe_short_name(base_class)
            };
            
            // Check if base class is in the same module or different
            // Extract module from base class name (e.g., "TopoDS_Shape" -> "TopoDS")
            let base_module = if let Some(underscore_pos) = base_class.find('_') {
                &base_class[..underscore_pos]
            } else {
                base_class.as_str()
            };
            let derived_module = &class.module;
            
            // Generate the C++ wrapper names
            let cpp_wrapper_name = format!("{}_as_{}", class.name, base_class);
            let cpp_wrapper_name_mut = format!("{}_as_{}_mut", class.name, base_class);
            
            // Generate Rust function names: 
            // - Same module: derived_as_base (e.g., face_as_shape)
            // - Different module: derived_as_module_base (e.g., make_cylindrical_hole_as_bop_algo_builder)
            // Using full name for different modules avoids collisions when two base classes
            // have the same short name (e.g., BRepFeat_Builder and BOPAlgo_Builder both being "Builder")
            let (rust_name, rust_name_mut) = if base_module == *derived_module {
                (
                    format_ident!(
                        "{}_as_{}",
                        heck::AsSnakeCase(&derived_short).to_string(),
                        heck::AsSnakeCase(&base_short).to_string()
                    ),
                    format_ident!(
                        "{}_as_{}_mut",
                        heck::AsSnakeCase(&derived_short).to_string(),
                        heck::AsSnakeCase(&base_short).to_string()
                    ),
                )
            } else {
                // Use full base class name (with module prefix) to avoid collisions
                (
                    format_ident!(
                        "{}_as_{}",
                        heck::AsSnakeCase(&derived_short).to_string(),
                        heck::AsSnakeCase(base_class).to_string()
                    ),
                    format_ident!(
                        "{}_as_{}_mut",
                        heck::AsSnakeCase(&derived_short).to_string(),
                        heck::AsSnakeCase(base_class).to_string()
                    ),
                )
            };
            
            // Determine the return types - if different module, use cross-module alias
            let (return_type, return_type_mut) = if base_module == *derived_module {
                // Same module - use short name directly
                let base_ident = format_ident!("{}", base_short);
                (
                    quote! { -> &#base_ident },
                    quote! { -> Pin<&mut #base_ident> },
                )
            } else {
                // Different module - the type alias will use the full C++ name
                let base_cpp = format_ident!("{}", base_class);
                (
                    quote! { -> &#base_cpp },
                    quote! { -> Pin<&mut #base_cpp> },
                )
            };
            
            // Doc comments
            let doc = format!("Upcast {} to {}", class.name, base_class);
            let doc_mut = format!("Upcast {} to {} (mutable)", class.name, base_class);

            vec![
                quote! {
                    #[doc = #doc]
                    #[cxx_name = #cpp_wrapper_name]
                    fn #rust_name(self_: &#derived_ident) #return_type;
                },
                quote! {
                    #[doc = #doc_mut]
                    #[cxx_name = #cpp_wrapper_name_mut]
                    fn #rust_name_mut(self_: Pin<&mut #derived_ident>) #return_type_mut;
                },
            ]
        })
        .collect()
}

/// Generate a to_owned function for cloning/copying an object via CXX's construct_unique
/// This generates: fn ClassName_to_owned(self_: &ClassName) -> UniquePtr<ClassName>;
/// with #[cxx_name = "construct_unique"] which uses CXX's built-in copy constructor support
/// Only generated for classes in modules known to have copy constructors.
fn generate_to_owned(class: &ParsedClass) -> Option<TokenStream> {
    // Skip for classes with protected destructors (can't create UniquePtr)
    if class.has_protected_destructor {
        return None;
    }
    
    // Skip for abstract classes (can't instantiate them)
    if class.is_abstract {
        return None;
    }
    
    // Only generate to_owned for modules where types are known to be copyable
    // TopoDS types (Shape, Edge, Face, etc.) and gp types are copyable
    // Many algorithm classes (like GeomAPI_*) have deleted copy constructors
    let copyable_modules = ["TopoDS", "gp", "TopLoc", "Bnd", "GProp"];
    
    if !copyable_modules.contains(&class.module.as_str()) {
        return None;
    }
    
    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);
    let fn_name = format_ident!("{}_to_owned", short_name);
    let doc = format!("Clone {} into a new UniquePtr via copy constructor", class.name);
    
    Some(quote! {
        #[doc = #doc]
        #[cxx_name = "construct_unique"]
        fn #fn_name(self_: &#rust_type) -> UniquePtr<#rust_type>;
    })
}

/// Generate a to_handle FFI function for transient classes
/// This generates: fn ClassName_to_handle(obj: UniquePtr<ClassName>) -> UniquePtr<HandleClassName>;
/// Only generated for classes that inherit from Standard_Transient (is_handle_type = true)
fn generate_to_handle_ffi(class: &ParsedClass) -> Option<TokenStream> {
    // Only generate for transient classes (those that can be wrapped in Handle)
    if !class.is_handle_type {
        return None;
    }

    // Skip classes with protected destructors (can't create UniquePtr)
    if class.has_protected_destructor {
        return None;
    }

    // Skip abstract classes (can't create UniquePtr)
    if class.is_abstract {
        return None;
    }

    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);
    
    // Handle type name: Handle{ClassName} with underscores removed
    let handle_type_name = format!("Handle{}", class.name.replace("_", ""));
    let handle_type_ident = format_ident!("{}", handle_type_name);
    
    // C++ wrapper name
    let cpp_name = format!("{}_to_handle", class.name);
    
    // Rust function name
    let fn_name = format_ident!("{}_to_handle", short_name);
    
    let doc = format!("Wrap {} in a Handle (reference-counted smart pointer)", class.name);

    Some(quote! {
        #[doc = #doc]
        #[cxx_name = #cpp_name]
        fn #fn_name(obj: UniquePtr<#rust_type>) -> UniquePtr<#handle_type_ident>;
    })
}

/// Generate Handle upcast FFI functions for inheritance
/// For each ancestor Handle type, generates functions to upcast to Handle of ancestor class
fn generate_handle_upcast_ffi(
    class: &ParsedClass,
    ctx: &TypeContext,
    symbol_table: &crate::resolver::SymbolTable,
) -> Vec<TokenStream> {
    // Only generate for transient classes
    if !class.is_handle_type {
        return Vec::new();
    }

    // Skip classes with protected destructors
    if class.has_protected_destructor {
        return Vec::new();
    }

    // NOTE: We DO generate handle upcasts for abstract classes.
    // Handle upcasting doesn't instantiate the class - it just copies the Handle
    // pointer to a base Handle type, which is always valid.

    let short_name = class.safe_short_name();
    
    // Handle type name for this class
    let handle_type_name = format!("Handle{}", class.name.replace("_", ""));
    let handle_type_ident = format_ident!("{}", handle_type_name);

    let ancestors = symbol_table.get_all_ancestors_by_name(&class.name);
    let protected_destructor_classes = symbol_table.protected_destructor_class_names();
    
    ancestors
        .iter()
        .filter_map(|base_class| {
            // Skip base classes that aren't in our known set of classes
            if !ctx.all_classes.contains(base_class) {
                return None;
            }

            // Skip base classes with protected destructors
            if protected_destructor_classes.contains(base_class) {
                return None;
            }

            // Base handle type name
            let base_handle_type_name = format!("Handle{}", base_class.replace("_", ""));
            let base_handle_type_ident = format_ident!("{}", base_handle_type_name);

            // Extract short name from base class for the Rust function name
            let base_short = if let Some(underscore_pos) = base_class.find('_') {
                crate::type_mapping::safe_short_name(&base_class[underscore_pos + 1..])
            } else {
                crate::type_mapping::safe_short_name(base_class)
            };

            // C++ wrapper name: HandleDerived_to_HandleBase
            let cpp_name = format!("{}_to_{}", handle_type_name, base_handle_type_name);
            
            // Rust function name: handle_derived_to_handle_base (snake_case)
            let fn_name = format_ident!(
                "{}_to_handle_{}",
                heck::AsSnakeCase(&short_name).to_string(),
                heck::AsSnakeCase(&base_short).to_string()
            );

            let doc = format!("Upcast Handle<{}> to Handle<{}>", class.name, base_class);

            Some(quote! {
                #[doc = #doc]
                #[cxx_name = #cpp_name]
                fn #fn_name(handle: &#handle_type_ident) -> UniquePtr<#base_handle_type_ident>;
            })
        })
        .collect()
}

/// Collect all methods from ancestor classes (direct and indirect parents)
/// Also returns a set of method names that are overridden in the inheritance chain
fn collect_ancestor_methods<'a>(
    class: &ParsedClass,
    all_classes: &'a HashMap<String, &'a ParsedClass>,
) -> (Vec<&'a Method>, HashSet<String>) {
    let mut methods = Vec::new();
    let mut visited = HashSet::new();
    let mut overridden_methods = HashSet::new();
    
    fn collect_from_parents<'b>(
        base_classes: &[String],
        all_classes: &'b HashMap<String, &'b ParsedClass>,
        methods: &mut Vec<&'b Method>,
        visited: &mut HashSet<String>,
        overridden_methods: &mut HashSet<String>,
    ) {
        for base_name in base_classes {
            // Avoid infinite recursion with cyclic inheritance (shouldn't happen, but be safe)
            if visited.contains(base_name) {
                continue;
            }
            visited.insert(base_name.clone());
            
            if let Some(parent) = all_classes.get(base_name) {
                // Track all methods declared in this parent (including protected/private)
                // so we know which methods are overridden somewhere in the hierarchy
                for method_name in &parent.all_method_names {
                    overridden_methods.insert(method_name.clone());
                }
                
                // Add public methods from this parent
                methods.extend(parent.methods.iter());
                
                // Recursively collect from grandparents
                collect_from_parents(&parent.base_classes, all_classes, methods, visited, overridden_methods);
            }
        }
    }
    
    collect_from_parents(&class.base_classes, all_classes, &mut methods, &mut visited, &mut overridden_methods);
    (methods, overridden_methods)
}

/// Generate inherited method declarations for a class
/// These are methods from base classes that CXX can call directly on the derived type
fn generate_inherited_methods(
    class: &ParsedClass,
    all_classes: &HashMap<String, &ParsedClass>,
    ctx: &TypeContext,
) -> Vec<TokenStream> {
    let short_name = class.safe_short_name();
    let rust_type = format_ident!("{}", short_name);
    let mut result = Vec::new();
    let mut seen_methods: HashSet<String> = HashSet::new();
    
    // Build a set of method names already declared directly on this class
    let existing_methods: HashSet<String> = class.methods.iter()
        .map(|m| m.name.clone())
        .collect();
    
    // Collect methods from all ancestor classes
    let (ancestor_methods, _overridden_in_hierarchy) = collect_ancestor_methods(class, all_classes);
    
    for method in ancestor_methods {
        // Skip if this class already declares this method directly (override)
        if existing_methods.contains(&method.name) {
            continue;
        }
        
        // Skip if this class has any method with this name (including protected/private overrides)
        // This prevents inheriting a public method from a grandparent when an intermediate
        // class has made it protected
        if class.all_method_names.contains(&method.name) {
            continue;
        }
        
        // Skip if we've already added this method (from a different ancestor)
        if seen_methods.contains(&method.name) {
            continue;
        }
        
        // Skip methods with unbindable types
        if method.has_unbindable_types() {
            continue;
        }
        
        // Skip methods with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
        if method_uses_enum(method, ctx.all_enums) {
            continue;
        }
        
        // Skip methods with class/handle params by value (not supported by CXX)
        if method.params.iter().any(|p| matches!(&p.ty, Type::Class(_) | Type::Handle(_))) {
            continue;
        }
        
        // Skip methods returning by value (would need wrapper - TODO)
        if let Some(ref ret_ty) = method.return_type {
            if matches!(ret_ty, Type::Class(_) | Type::Handle(_)) {
                continue;
            }
        }
        
        // Skip const methods that return mutable references (CXX doesn't allow this)
        if has_const_mut_return_mismatch(method) {
            continue;
        }
        
        seen_methods.insert(method.name.clone());
        
        // Generate method declaration similar to generate_methods()
        let cpp_name = &method.name;
        let rust_name = format_ident!("{}", safe_method_name(cpp_name));
        
        // Parameters, escaping keywords
        let params = method.params.iter().map(|p| {
            let name = safe_param_ident(&p.name);
            let ty = map_type_in_context(&p.ty, ctx);
            let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
            quote! { #name: #ty_tokens }
        });
        
        // Return type
        let return_type = method.return_type.as_ref().map(|ty| {
            let mapping = map_return_type_in_context(ty, ctx);
            let ty_tokens: TokenStream = mapping.rust_type.parse().unwrap_or_else(|_| quote! { () });
            quote! { -> #ty_tokens }
        });
        
        // Doc comment
        let doc = method
            .comment
            .as_ref()
            .map(|c| quote! { #[doc = #c] })
            .unwrap_or_else(|| {
                let default_doc = format!("Inherited from base class");
                quote! { #[doc = #default_doc] }
            });
        
        // Generate method based on const-ness
        let method_decl = if method.is_const {
            quote! {
                #doc
                #[cxx_name = #cpp_name]
                fn #rust_name(self: &#rust_type, #(#params),*) #return_type;
            }
        } else {
            quote! {
                #doc
                #[cxx_name = #cpp_name]
                fn #rust_name(self: Pin<&mut #rust_type>, #(#params),*) #return_type;
            }
        };
        
        result.push(method_decl);
    }
    
    result
}

/// Generate namespace-level free function declarations
fn generate_functions(functions: &[&ParsedFunction], ctx: &TypeContext) -> Vec<TokenStream> {
    // Track function names to detect overloads
    let mut seen_names: std::collections::HashMap<String, usize> = std::collections::HashMap::new();
    
    functions
        .iter()
        .filter_map(|func| {
            // Skip functions with unbindable types
            if func.has_unbindable_types() {
                return None;
            }
            
            // Skip functions with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
            if function_uses_enum(func, ctx.all_enums) {
                return None;
            }
            
            // Generate a C++ wrapper name that won't clash with type names
            // Pattern: namespace_fn_shortname (e.g., TopoDS_fn_Vertex instead of TopoDS_Vertex)
            let base_wrapper_name = format!("{}_fn_{}", func.namespace, func.short_name);
            
            // Generate Rust function name: snake_case of short name (e.g., vertex)
            let base_rust_name = func.short_name.to_snake_case();
            
            // Check if this is a const or mut version (for overloads)
            let is_mut_version = func.params.iter().any(|p| matches!(&p.ty, Type::MutRef(_)));
            
            // Track overloads and give unique names
            let count = seen_names.entry(base_rust_name.clone()).or_insert(0);
            *count += 1;
            
            // If this is an overload (count > 1), add a suffix
            // For the common case of const/mut overloads, use _mut suffix
            let (cpp_wrapper_name, rust_name) = if *count > 1 {
                let suffix = if is_mut_version { "_mut" } else { &format!("_{}", count) };
                (format!("{}{}", base_wrapper_name, suffix), format!("{}{}", base_rust_name, suffix))
            } else {
                (base_wrapper_name, base_rust_name)
            };
            
            let rust_name_ident = format_ident!("{}", rust_name);
            
            // Parameters, escaping keywords
            let params = func.params.iter().map(|p| {
                let name = safe_param_ident(&p.name);
                let ty = map_type_in_context(&p.ty, ctx);
                let ty_tokens: TokenStream = ty.rust_type.parse().unwrap_or_else(|_| quote! { () });
                quote! { #name: #ty_tokens }
            });
            
            // Return type
            let return_type = func.return_type.as_ref().map(|ty| {
                let mapping = map_return_type_in_context(ty, ctx);
                let ty_tokens: TokenStream = mapping.rust_type.parse().unwrap_or_else(|_| quote! { () });
                quote! { -> #ty_tokens }
            });
            
            // Doc comment
            let doc = func
                .comment
                .as_ref()
                .map(|c| quote! { #[doc = #c] })
                .unwrap_or_else(|| {
                    let default_doc = format!("{}::{}", func.namespace, func.short_name);
                    quote! { #[doc = #default_doc] }
                });
            
            Some(quote! {
                #doc
                #[cxx_name = #cpp_wrapper_name]
                fn #rust_name_ident(#(#params),*) #return_type;
            })
        })
        .collect()
}

/// Generate impl UniquePtr blocks for types defined in this module
fn generate_unique_ptr_impls(classes: &[&ParsedClass]) -> Vec<TokenStream> {
    classes
        .iter()
        .map(|class| {
            let rust_type = format_ident!("{}", class.safe_short_name());
            quote! {
                impl UniquePtr<#rust_type> {}
            }
        })
        .collect()
}

/// Generate re-export statements and impl blocks with constructor methods
fn generate_re_exports(
    _module_name: &str,
    classes: &[&ParsedClass],
    enums: &[&ParsedEnum],
    functions: &[&ParsedFunction],
    all_classes: &HashSet<String>,
    protected_destructor_classes: &HashSet<String>,
    all_enums: &HashSet<String>,
    symbol_table: &crate::resolver::SymbolTable,
) -> Vec<TokenStream> {
    use crate::type_mapping::is_reserved_name;
    
    // Build set of class names owned by this module (use short names in ffi)
    let owned_classes: HashSet<String> = classes.iter().map(|c| c.name.clone()).collect();
    
    let mut exports = Vec::new();

    // Re-export namespace-level free functions
    // The functions in ffi already use snake_case names (e.g., `vertex`, `vertex_mut`)
    // so we just need to re-export them with the same names
    // Track function names to detect overloads (must match generate_functions logic)
    let mut seen_names: HashMap<String, usize> = HashMap::new();
    
    for func in functions {
        // Skip functions with unbindable types
        if func.has_unbindable_types() {
            continue;
        }
        
        // Track overloads (must match generate_functions logic)
        let base_rust_name = func.short_name.to_snake_case();
        let is_mut_version = func.params.iter().any(|p| matches!(&p.ty, Type::MutRef(_)));
        
        let count = seen_names.entry(base_rust_name.clone()).or_insert(0);
        *count += 1;
        
        // Generate the Rust function name (must match generate_functions logic)
        let rust_name = if *count > 1 {
            let suffix = if is_mut_version { "_mut" } else { &format!("_{}", count) };
            format!("{}{}", base_rust_name, suffix)
        } else {
            base_rust_name
        };
        
        let rust_name_ident = format_ident!("{}", rust_name);
        
        // Re-export the function from ffi with its Rust name
        exports.push(quote! {
            pub use ffi::#rust_name_ident;
        });
    }

    // NOTE: Enum re-exports are disabled because enum generation is disabled
    // (OCCT uses unscoped enums which CXX doesn't support)
    let _ = enums; // suppress unused warning

    for class in classes {
        let short_name = class.short_name();
        let safe_name = class.safe_short_name();
        
        if is_reserved_name(short_name) {
            // For reserved names, re-export with alias: pub use ffi::Vec_ as Vec;
            let ffi_type = format_ident!("{}", safe_name);  // Vec_
            let pub_type = format_ident!("{}", short_name); // Vec
            exports.push(quote! {
                pub use ffi::#ffi_type as #pub_type;
            });
        } else {
            let rust_type = format_ident!("{}", short_name);
            exports.push(quote! {
                pub use ffi::#rust_type;
            });
        }

        // Generate impl block with constructor methods, upcast methods, and to_owned
        // Get all transitive base classes that are in our parsed set and don't have protected destructors
        let all_ancestors = symbol_table.get_all_ancestors_by_name(&class.name);
        let valid_base_classes: Vec<_> = all_ancestors.iter()
            .filter(|bc| all_classes.contains(*bc) && !protected_destructor_classes.contains(*bc))
            .collect();
        
        // Always generate impl block (at minimum for to_owned method)
        {
            let rust_type = format_ident!("{}", short_name);
            let mut methods: Vec<TokenStream> = Vec::new();
            
            // Generate constructor methods (skip for classes with protected destructors or abstract classes)
            if !class.constructors.is_empty() && !class.has_protected_destructor && !class.is_abstract {
                // Track constructor names to handle duplicates
                let mut ctor_names: std::collections::HashMap<String, usize> = std::collections::HashMap::new();
                
                // Filter constructors the same way as generate_constructors does
                let ctor_methods: Vec<TokenStream> = class.constructors.iter()
                    .filter(|ctor| {
                        // Skip constructors with class/handle parameters passed by value (not supported by CXX)
                        if ctor.params.iter().any(|p| matches!(&p.ty, Type::Class(_) | Type::Handle(_))) {
                            return false;
                        }
                        // Skip constructors with unbindable types (C strings, streams, void pointers, etc.)
                        if ctor.has_unbindable_types() {
                            return false;
                        }
                        true
                    })
                    .map(|ctor| {
                    let base_suffix = ctor.overload_suffix();
                    let method_name = if base_suffix.is_empty() {
                        "new".to_string()
                    } else {
                        format!("new{}", base_suffix)
                    };
                    
                    // Handle duplicate method names by adding a number
                    let count = ctor_names.entry(method_name.clone()).or_insert(0);
                    *count += 1;
                    let final_method_name = if *count > 1 {
                        format!("{}_{}", method_name, count)
                    } else {
                        method_name
                    };
                    let method_ident = format_ident!("{}", final_method_name.to_snake_case());
                    
                    // Generate the ffi function name (must match what generate_constructors produces)
                    let ffi_suffix = if base_suffix.is_empty() {
                        "ctor".to_string()
                    } else {
                        format!("ctor{}", base_suffix)
                    };
                    let ffi_fn_name = format_ident!(
                        "{}_{}",
                        safe_name,
                        ffi_suffix.to_snake_case()
                    );
                    
                    // Generate parameter list for method signature - keep original C++ names
                    // For types in the ffi module, we need to use the correct name:
                    // - Owned classes use short names (Pnt, Vec_, etc.)
                    // - Opaque/external types use full C++ names (gp_Trsf2d, etc.)
                    // Parameter names must be escaped if they are Rust keywords
                    let params: Vec<TokenStream> = ctor.params.iter().map(|p| {
                        let name = safe_param_ident(&p.name);
                        let ty_str = type_to_ffi_string(&p.ty, &owned_classes, all_enums, false);
                        let ty_tokens: TokenStream = ty_str.parse().unwrap_or_else(|_| quote! { () });
                        quote! { #name: #ty_tokens }
                    }).collect();
                    
                    // Generate argument list for calling ffi function
                    let args: Vec<TokenStream> = ctor.params.iter().map(|p| {
                        let name = safe_param_ident(&p.name);
                        quote! { #name }
                    }).collect();
                    
                    // Doc comment
                    let doc = ctor
                        .comment
                        .as_ref()
                        .map(|c| quote! { #[doc = #c] })
                        .unwrap_or_default();
                    
                    quote! {
                        #doc
                        pub fn #method_ident(#(#params),*) -> cxx::UniquePtr<Self> {
                            ffi::#ffi_fn_name(#(#args),*)
                        }
                    }
                }).collect();
                
                methods.extend(ctor_methods);
            }
            
            // Generate upcast methods (e.g., as_shape() for TopoDS types)
            // Only for base classes that are in our parsed set (valid_base_classes)
            for base_class in &valid_base_classes {
                // Extract module from base class (e.g., "BRepBuilderAPI_MakeShape" -> "BRepBuilderAPI")
                let base_module = if let Some(underscore_pos) = base_class.find('_') {
                    &base_class[..underscore_pos]
                } else {
                    base_class.as_str()
                };
                
                // Extract short name from base class (e.g., "TopoDS_Shape" -> "Shape")
                let base_short = if let Some(underscore_pos) = base_class.find('_') {
                    crate::type_mapping::safe_short_name(&base_class[underscore_pos + 1..])
                } else {
                    crate::type_mapping::safe_short_name(base_class)
                };
                
                // Generate the ffi function name: 
                // - Same module: derived_as_base (e.g., vertex_as_shape)
                // - Different module: derived_as_module_base (e.g., make_cylindrical_hole_as_bop_algo_builder)
                // This must match the naming in generate_upcast_functions
                let derived_snake = heck::AsSnakeCase(&safe_name).to_string();
                let ffi_fn_name = if base_module == class.module {
                    let base_snake = heck::AsSnakeCase(&base_short).to_string();
                    format_ident!("{}_as_{}", derived_snake, base_snake)
                } else {
                    let base_snake_full = heck::AsSnakeCase(base_class.as_str()).to_string();
                    format_ident!("{}_as_{}", derived_snake, base_snake_full)
                };
                
                // Generate method name: as_base (e.g., as_shape) or as_module_base for different modules
                let method_name = if base_module == class.module {
                    let base_snake = heck::AsSnakeCase(&base_short).to_string();
                    format_ident!("as_{}", base_snake)
                } else {
                    let base_snake_full = heck::AsSnakeCase(base_class.as_str()).to_string();
                    format_ident!("as_{}", base_snake_full)
                };
                
                // Determine return type - use short name if in same module, full path otherwise
                let base_type: TokenStream = if base_module == class.module {
                    // Same module - use short name directly
                    base_short.parse().unwrap_or_else(|_| quote! { () })
                } else {
                    // Different module - use full path
                    let rust_module = crate::module_graph::module_to_rust_name(base_module);
                    format!("crate::{}::{}", rust_module, base_short).parse().unwrap_or_else(|_| quote! { () })
                };
                
                let doc = format!("Upcast to {}", base_class);
                
                methods.push(quote! {
                    #[doc = #doc]
                    pub fn #method_name(&self) -> &#base_type {
                        ffi::#ffi_fn_name(self)
                    }
                });
                
                // Generate mutable upcast method
                let ffi_fn_name_mut = format_ident!("{}_mut", ffi_fn_name);
                let method_name_mut = format_ident!("{}_mut", method_name);
                let doc_mut = format!("Upcast to {} (mutable)", base_class);
                
                methods.push(quote! {
                    #[doc = #doc_mut]
                    pub fn #method_name_mut(self: std::pin::Pin<&mut Self>) -> std::pin::Pin<&mut #base_type> {
                        ffi::#ffi_fn_name_mut(self)
                    }
                });
            }
            
            // Generate to_owned method (clone/copy via construct_unique)
            // Only for modules with copyable types, and skip classes with protected destructors or abstract classes
            let copyable_modules = ["TopoDS", "gp", "TopLoc", "Bnd", "GProp"];
            if copyable_modules.contains(&class.module.as_str()) && !class.has_protected_destructor && !class.is_abstract {
                let to_owned_fn = format_ident!("{}_to_owned", safe_name);
                methods.push(quote! {
                    #[doc = "Clone into a new UniquePtr via copy constructor"]
                    pub fn to_owned(&self) -> cxx::UniquePtr<Self> {
                        ffi::#to_owned_fn(self)
                    }
                });
            }

            // Generate to_handle() associated function for transient classes
            // This wraps the object in a Handle (reference-counted smart pointer)
            // It's an associated function (not a method) because UniquePtr<Self> receivers
            // require the unstable arbitrary_self_types feature
            if class.is_handle_type && !class.has_protected_destructor && !class.is_abstract {
                let ffi_fn_name = format_ident!("{}_to_handle", safe_name);
                let handle_type_name = format!("Handle{}", class.name.replace("_", ""));
                let handle_type_ident = format_ident!("{}", handle_type_name);
                let doc = format!("Wrap {} in a Handle (reference-counted smart pointer)", class.name);
                
                methods.push(quote! {
                    #[doc = #doc]
                    pub fn to_handle(obj: cxx::UniquePtr<Self>) -> cxx::UniquePtr<ffi::#handle_type_ident> {
                        ffi::#ffi_fn_name(obj)
                    }
                });
            }
            
            // Generate wrapper methods for instance methods returning class types by value
            // These call ffi free functions that wrap C++ methods
            let wrapper_method_impls = generate_wrapper_method_impls(class, &owned_classes, all_enums);
            methods.extend(wrapper_method_impls);
            
            // Generate static method wrappers
            // These call ffi free functions that wrap C++ static methods
            let static_method_impls = generate_static_method_impls(class, &owned_classes, all_enums);
            methods.extend(static_method_impls);
            
            exports.push(quote! {
                impl #rust_type {
                    #(#methods)*
                }
            });
        }
    }

    exports
}

/// Generate impl block methods for wrapper functions (methods returning class types by value)
fn generate_wrapper_method_impls(
    class: &ParsedClass,
    owned_classes: &HashSet<String>,
    all_enums: &HashSet<String>,
) -> Vec<TokenStream> {
    let short_name = class.short_name();
    let safe_name = class.safe_short_name();
    
    // Group methods by name to handle overloads
    let mut method_counts: HashMap<String, usize> = HashMap::new();
    
    class
        .methods
        .iter()
        .filter_map(|method| {
            // Only process methods that need wrapper functions
            if !needs_wrapper_function(method, all_enums) {
                return None;
            }
            
            // Skip methods with class/handle parameters passed by value (not supported by CXX)
            if has_unsupported_by_value_params(method) {
                return None;
            }

            // Skip methods with unbindable types (streams, void pointers, etc.)
            if method.has_unbindable_types() {
                return None;
            }

            // Skip methods with enum types (enums not supported - CXX uses enum class but OCCT uses unscoped enums)
            if method_uses_enum(method, all_enums) {
                return None;
            }

            // Skip methods that need explicit lifetimes (Pin<&mut Self> return with reference params)
            if needs_explicit_lifetimes(method) {
                return None;
            }

            let count = method_counts.entry(method.name.clone()).or_insert(0);
            *count += 1;

            // Generate a suffix based on parameters for overloaded methods
            // Must match the logic in generate_overload_suffix_for_wrappers
            let same_name_count = class
                .methods
                .iter()
                .filter(|m| m.name == method.name 
                    && needs_wrapper_function(m, all_enums)
                    && !method_uses_enum(m, all_enums)
                    && !needs_explicit_lifetimes(m))
                .count();

            // Check if there are any non-wrapper methods with the same name
            // (These would conflict with the wrapper impl method name)
            let has_non_wrapper_with_same_name = class
                .methods
                .iter()
                .any(|m| m.name == method.name && !needs_wrapper_function(m, all_enums) 
                    && !has_unsupported_by_value_params(m)
                    && !m.has_unbindable_types()
                    && !method_uses_enum(m, all_enums)
                    && !has_const_mut_return_mismatch(m)
                    && !needs_explicit_lifetimes(m));

            let overload_suffix = if same_name_count <= 1 && !has_non_wrapper_with_same_name {
                String::new()
            } else {
                let base_suffix = method.overload_suffix();
                
                // Check if there's another method with the same name and parameter types but different const-ness
                let has_const_mut_pair = class
                    .methods
                    .iter()
                    .filter(|m| m.name == method.name 
                        && needs_wrapper_function(m, all_enums)
                        && !method_uses_enum(m, all_enums)
                        && !needs_explicit_lifetimes(m))
                    .any(|m| m.overload_suffix() == base_suffix && m.is_const != method.is_const);
                
                if has_const_mut_pair && !method.is_const {
                    format!("{}_mut", base_suffix)
                } else {
                    base_suffix
                }
            };

            // ffi function name: ClassName_method_name with optional suffix
            // overload_suffix is already in snake_case with leading underscore (e.g., "_pnt")
            let ffi_fn_name = if overload_suffix.is_empty() {
                format_ident!("{}_{}", safe_name, method.name.to_snake_case())
            } else {
                format_ident!(
                    "{}_{}{}" ,
                    safe_name,
                    method.name.to_snake_case(),
                    overload_suffix
                )
            };

            // Public method name: just method_name (snake_case) with optional suffix
            let method_name = if overload_suffix.is_empty() {
                format_ident!("{}", method.name.to_snake_case())
            } else {
                format_ident!(
                    "{}{}",
                    method.name.to_snake_case(),
                    overload_suffix
                )
            };

            // Parameters (not including self), escaping keywords
            let params: Vec<TokenStream> = method.params.iter().map(|p| {
                let name = safe_param_ident(&p.name);
                let ty_str = type_to_ffi_string(&p.ty, owned_classes, all_enums, false);
                let ty_tokens: TokenStream = ty_str.parse().unwrap_or_else(|_| quote! { () });
                quote! { #name: #ty_tokens }
            }).collect();

            // Argument list for calling ffi function (includes self)
            let args: Vec<TokenStream> = method.params.iter().map(|p| {
                let name = safe_param_ident(&p.name);
                quote! { #name }
            }).collect();

            // Return type - wrap in UniquePtr for class types, but NOT for enums
            let return_type = method.return_type.as_ref().map(|ty| {
                let ty_str = type_to_ffi_string(ty, owned_classes, all_enums, true);
                // Wrap in UniquePtr for class types returned by value, but NOT enums
                let is_enum = matches!(ty, Type::Class(name) if all_enums.contains(name));
                if (ty.is_class() || ty.is_handle()) && !is_enum {
                    format!("cxx::UniquePtr<{}>", ty_str)
                } else {
                    ty_str
                }
            });
            let return_type_tokens: TokenStream = return_type
                .as_ref()
                .map(|t| t.parse().unwrap_or_else(|_| quote! { () }))
                .unwrap_or_else(|| quote! { () });

            // Doc comment
            let doc = method
                .comment
                .as_ref()
                .map(|c| quote! { #[doc = #c] })
                .unwrap_or_default();

            // Generate method with self
            if method.is_const {
                Some(quote! {
                    #doc
                    pub fn #method_name(&self, #(#params),*) -> #return_type_tokens {
                        ffi::#ffi_fn_name(self, #(#args),*)
                    }
                })
            } else {
                Some(quote! {
                    #doc
                    pub fn #method_name(self: std::pin::Pin<&mut Self>, #(#params),*) -> #return_type_tokens {
                        ffi::#ffi_fn_name(self, #(#args),*)
                    }
                })
            }
        })
        .collect()
}

/// Generate impl block methods for static methods
fn generate_static_method_impls(
    class: &ParsedClass,
    owned_classes: &HashSet<String>,
    all_enums: &HashSet<String>,
) -> Vec<TokenStream> {
    let _short_name = class.short_name();
    let safe_name = class.safe_short_name();
    
    // Track method counts for overload disambiguation
    let mut method_counts: HashMap<String, usize> = HashMap::new();
    
    class
        .static_methods
        .iter()
        .filter_map(|method| {
            // Skip methods with stream parameters - CXX can't bind C++ streams
            if method.has_unbindable_types() {
                return None;
            }

            // Skip methods with enum types (enums not supported)
            if static_method_uses_enum(method, all_enums) {
                return None;
            }

            // Skip static methods that return bare references - these need lifetimes
            if method.return_type.as_ref().map(|t| t.is_reference()).unwrap_or(false) {
                return None;
            }
            
            // Skip static methods with class/handle parameters passed by value (not supported by CXX)
            if method.params.iter().any(|p| matches!(&p.ty, Type::Class(_) | Type::Handle(_))) {
                return None;
            }
            
            let count = method_counts.entry(method.name.clone()).or_insert(0);
            *count += 1;
            
            // Count how many static methods have this name to determine if suffix is needed
            let static_same_name_count = class
                .static_methods
                .iter()
                .filter(|m| m.name == method.name)
                .count();
            
            // Also check for instance methods with the same name (they generate wrapper functions
            // with the same naming pattern: ClassName_method_name)
            let instance_same_name_exists = class
                .methods
                .iter()
                .any(|m| m.name == method.name);
            
            // Generate overload suffix based on parameter types
            // We need a suffix if:
            // 1. There are multiple static methods with this name, OR
            // 2. There's an instance method with the same name
            let overload_suffix = if static_same_name_count > 1 || instance_same_name_exists {
                method.overload_suffix()
            } else {
                String::new()
            };
            
            // ffi function name: ClassName_MethodName with optional suffix
            let ffi_fn_name = format_ident!(
                "{}_{}{}", 
                safe_name,
                method.name.to_snake_case(),
                overload_suffix
            );

            // Public method name: just method_name (snake_case) with optional suffix
            // overload_suffix is already in snake_case with leading underscore (e.g., "_pnt")
            let method_name = if overload_suffix.is_empty() {
                format_ident!("{}", method.name.to_snake_case())
            } else {
                format_ident!(
                    "{}{}",
                    method.name.to_snake_case(),
                    overload_suffix
                )
            };

            // Parameters, escaping keywords
            let params: Vec<TokenStream> = method.params.iter().map(|p| {
                let name = safe_param_ident(&p.name);
                let ty_str = type_to_ffi_string(&p.ty, owned_classes, all_enums, false);
                let ty_tokens: TokenStream = ty_str.parse().unwrap_or_else(|_| quote! { () });
                quote! { #name: #ty_tokens }
            }).collect();

            // Argument list for calling ffi function
            let args: Vec<TokenStream> = method.params.iter().map(|p| {
                let name = safe_param_ident(&p.name);
                quote! { #name }
            }).collect();

            // Return type - wrap in UniquePtr for class types, but NOT for enums
            let return_type = method.return_type.as_ref().map(|ty| {
                let ty_str = type_to_ffi_string(ty, owned_classes, all_enums, true);
                // Wrap in UniquePtr for class types returned by value, but NOT enums
                let is_enum = matches!(ty, Type::Class(name) if all_enums.contains(name));
                if (ty.is_class() || ty.is_handle()) && !is_enum {
                    format!("cxx::UniquePtr<{}>", ty_str)
                } else {
                    ty_str
                }
            });
            
            let return_type_tokens: Option<TokenStream> = return_type
                .as_ref()
                .map(|t| {
                    let parsed: TokenStream = t.parse().unwrap_or_else(|_| quote! { () });
                    quote! { -> #parsed }
                });

            // Doc comment
            let doc = method
                .comment
                .as_ref()
                .map(|c| quote! { #[doc = #c] })
                .unwrap_or_default();

            Some(quote! {
                #doc
                pub fn #method_name(#(#params),*) #return_type_tokens {
                    ffi::#ffi_fn_name(#(#args),*)
                }
            })
        })
        .collect()
}

/// Convert a Type to a string suitable for use in impl blocks outside ffi.
/// owned_classes contains the full C++ names of classes defined in this module.
/// all_enums contains the full C++ names of all enums.
/// is_return: true if this type is used as a return type (const char* -> String)
///            false if this type is used as a parameter (const char* -> &str)
fn type_to_ffi_string(ty: &Type, owned_classes: &HashSet<String>, all_enums: &HashSet<String>, is_return: bool) -> String {
    use crate::type_mapping::is_reserved_name;
    
    match ty {
        Type::Void => "()".to_string(),
        Type::Bool => "bool".to_string(),
        Type::I32 => "i32".to_string(),
        Type::U32 => "u32".to_string(),
        Type::I64 => "i64".to_string(),
        Type::U64 => "u64".to_string(),
        Type::Usize => "usize".to_string(),
        Type::F32 => "f32".to_string(),
        Type::F64 => "f64".to_string(),
        Type::ConstRef(inner) => {
            let inner_str = type_to_ffi_string(inner, owned_classes, all_enums, is_return);
            format!("&{}", inner_str)
        }
        Type::MutRef(inner) => {
            let inner_str = type_to_ffi_string(inner, owned_classes, all_enums, is_return);
            // Mutable references to class types need Pin in CXX
            if inner.is_class() || inner.is_handle() {
                format!("std::pin::Pin<&mut {}>", inner_str)
            } else {
                format!("&mut {}", inner_str)
            }
        }
        Type::RValueRef(_) => {
            // RValueRef types should be filtered out before reaching here
            panic!("RValueRef types should not be converted to FFI strings - they are unbindable")
        }
        Type::ConstPtr(inner) => {
            // const char* handling differs for return types vs parameters
            if matches!(inner.as_ref(), Type::Class(name) if name == "char") {
                if is_return {
                    // Return type: const char* maps to String (wrapper converts via rust::String)
                    "String".to_string()
                } else {
                    // Parameter type: const char* maps to &str in CXX
                    "&str".to_string()
                }
            } else {
                let inner_str = type_to_ffi_string(inner, owned_classes, all_enums, is_return);
                format!("*const {}", inner_str)
            }
        }
        Type::MutPtr(inner) => {
            let inner_str = type_to_ffi_string(inner, owned_classes, all_enums, is_return);
            format!("*mut {}", inner_str)
        }
        Type::Handle(name) => {
            // Use full class name (without underscores) to match declaration and avoid collisions
            // e.g., "Geom_Surface" -> "HandleGeomSurface"
            format!("ffi::Handle{}", name.replace("_", ""))
        }
        Type::Class(name) => {
            // Check if this is actually an enum
            if all_enums.contains(name) {
                // For enums, extract module and short name
                // Enums like "TopAbs_ShapeEnum" -> module "TopAbs", short "ShapeEnum"
                // Same-module enums use ffi::ShortName
                // Cross-module enums use crate::module_snake::ShortName
                let (module, short_name) = if let Some(underscore_pos) = name.find('_') {
                    (&name[..underscore_pos], &name[underscore_pos + 1..])
                } else {
                    ("", name.as_str())
                };
                
                // Check if this enum is in the same module as a class we own
                // If so, use ffi:: prefix, otherwise use crate::module:: path
                let is_same_module = owned_classes.iter().any(|c| {
                    c.find('_').map(|pos| &c[..pos]) == Some(module)
                });
                
                if is_same_module {
                    return format!("ffi::{}", short_name);
                } else {
                    // Cross-module enum - use full crate path
                    let module_snake = heck::AsSnakeCase(module).to_string();
                    return format!("crate::{}::{}", module_snake, short_name);
                }
            }
            
            if owned_classes.contains(name) {
                // Owned class - use short name (with reserved name handling)
                let short_name = if let Some(underscore_pos) = name.find('_') {
                    &name[underscore_pos + 1..]
                } else {
                    name.as_str()
                };
                let safe_name = if is_reserved_name(short_name) {
                    format!("{}_", short_name)
                } else {
                    short_name.to_string()
                };
                format!("ffi::{}", safe_name)
            } else {
                // External/opaque type - use full C++ name as declared in ffi
                format!("ffi::{}", name)
            }
        }
    }
}

/// Generate impl blocks for Handle types with upcast methods
/// For each transient class, generate an impl block for its Handle type with methods
/// to upcast to base Handle types
fn generate_handle_impls(
    classes: &[&ParsedClass],
    all_classes: &HashSet<String>,
    protected_destructor_classes: &HashSet<String>,
    symbol_table: &crate::resolver::SymbolTable,
) -> Vec<TokenStream> {
    let mut impls = Vec::new();

    for class in classes {
        // Only generate for transient classes (those with Handle types)
        if !class.is_handle_type {
            continue;
        }

        // Skip classes with protected destructors
        if class.has_protected_destructor {
            continue;
        }

        // Handle type name for this class
        let handle_type_name = format!("Handle{}", class.name.replace("_", ""));
        let handle_type_ident = format_ident!("{}", handle_type_name);

        let short_name = class.safe_short_name();
        let mut methods: Vec<TokenStream> = Vec::new();

        // Generate upcast methods for each ancestor class
        let ancestors = symbol_table.get_all_ancestors_by_name(&class.name);
        for base_class in &ancestors {
            // Skip base classes that aren't in our known set
            if !all_classes.contains(base_class) {
                continue;
            }

            // Skip base classes with protected destructors
            if protected_destructor_classes.contains(base_class) {
                continue;
            }

            // Base handle type name
            let base_handle_type_name = format!("Handle{}", base_class.replace("_", ""));
            let base_handle_type_ident = format_ident!("{}", base_handle_type_name);

            // Extract short name from base class for method name
            let base_short = if let Some(underscore_pos) = base_class.find('_') {
                crate::type_mapping::safe_short_name(&base_class[underscore_pos + 1..])
            } else {
                crate::type_mapping::safe_short_name(base_class)
            };

            // ffi function name (must match generate_handle_upcast_ffi)
            let ffi_fn_name = format_ident!(
                "{}_to_handle_{}",
                heck::AsSnakeCase(&short_name).to_string(),
                heck::AsSnakeCase(&base_short).to_string()
            );

            // Method name: to_handle_base (e.g., to_handle_curve)
            let method_name = format_ident!("to_handle_{}", heck::AsSnakeCase(&base_short).to_string());

            let doc = format!("Upcast to Handle<{}>", base_class);

            methods.push(quote! {
                #[doc = #doc]
                pub fn #method_name(&self) -> cxx::UniquePtr<ffi::#base_handle_type_ident> {
                    ffi::#ffi_fn_name(self)
                }
            });
        }

        // Only generate impl block if there are methods
        if !methods.is_empty() {
            impls.push(quote! {
                impl ffi::#handle_type_ident {
                    #(#methods)*
                }
            });
        }
    }

    impls
}/// Generate to_handle free function exports
